/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_TIM4_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_3);
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_4);
  TIM4->PSC = 200;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  char message[25];

  while (1)
  {
	  // X
	  HAL_ADC_Start(&hadc1);

	  HAL_ADC_PollForConversion(&hadc1, 10);

	  uint16_t value = HAL_ADC_GetValue(&hadc1);



	  TIM4->CCR3 = value;

	  sprintf(message, "\fJoy2 X: %d\r\n",value);


	  HAL_UART_Transmit(&huart2, (uint8_t*)message, strlen(message), 10);


	  HAL_ADC_PollForConversion(&hadc1, 10);

	  value = HAL_ADC_GetValue(&hadc1);

	  TIM4->CCR4 = value;

	  sprintf(message, "Joy2 Y: %d",value);

	  HAL_UART_Transmit(&huart2, (uint8_t*)message, strlen(message), 10);

	  HAL_ADC_Stop(&hadc1);


	  uint8_t rmessage = 0;
	  HAL_StatusTypeDef status = HAL_UART_Receive(&huart2, &rmessage, 1, 10);
	  if(status == HAL_OK)
	  {
		  switch (rmessage) {
			case 1:TIM4->CCR1 = 4000;
				break;
			case 2:TIM4->CCR1 = 0;
				break;
			case 3:TIM4->CCR2 = 4000;
				break;
			case 4:TIM4->CCR2 = 0;
				break;
		  }
	  }

	  HAL_Delay(10);
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

